import React from 'react';
import { useNavigate } from 'react-router-dom';
import { Action } from '../../config/roleConfig';
import { useAuth } from '../../../../context/AuthContext';

interface QuickActionsProps {
  actions: Action[];
}

export const QuickActions: React.FC<QuickActionsProps> = ({ actions }) => {
  const navigate = useNavigate();
  const { user } = useAuth();
  const role = user?.role?.toLowerCase();

  const handleAction = (action: Action) => {
    if (action.handler) {
      // Handle custom action handlers
      console.log(`Handling action: ${action.handler}`);
    } else {
      // Default navigation based on action name
      const routeMap: Record<string, string> = {
        create: `/${role}/create-load`,
        match: `/${role}/smart-load-match`,
        carriers: `/${role}/carrier-match`,
        reports: `/${role}/reports`,
        search: `/${role}/loads`,
        schedule: `/${role}/schedule`,
        filter: `/${role}/loads?filter=true`
      };
      
      const route = routeMap[action.name];
      if (route) {
        navigate(route);
      }
    }
  };
  
  return (
    <section className="mb-8">
      <h2 className="text-xl font-semibold mb-4 flex items-center gap-2">
        <svg className="w-5 h-5 text-blue-600" fill="none" stroke="currentColor" viewBox="0 0 24 24">
          <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M13 10V3L4 14h7v7l9-11h-7z" />
        </svg>
        Quick Actions
      </h2>
      <div className="grid grid-cols-2 md:grid-cols-4 gap-4">
        {actions.map((action, index) => (
          <button
            key={`${action.name}-${index}`}
            onClick={() => handleAction(action)}
            className="p-6 bg-white rounded-lg shadow-sm hover:shadow-md transition-all border border-gray-200 text-center group"
          >
            <div className="w-12 h-12 bg-blue-100 rounded-full flex items-center justify-center mx-auto mb-3 group-hover:bg-blue-200 transition-colors">
              {getActionIcon(action.icon || action.name)}
            </div>
            <span className="text-sm font-medium text-gray-700 capitalize">
              {action.name.replace(/_/g, ' ')}
            </span>
          </button>
        ))}
      </div>
    </section>
  );
};

